function generatePlaceholderUrl(width, height, colors, text) {
  const [bg, fg, accent] = colors;
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${height}">
    <defs>
      <linearGradient id="g" x1="0%" y1="0%" x2="100%" y2="100%">
        <stop offset="0%" style="stop-color:${bg}"/>
        <stop offset="100%" style="stop-color:${fg}"/>
      </linearGradient>
    </defs>
    <rect width="${width}" height="${height}" fill="url(#g)"/>
    <circle cx="${width * 0.7}" cy="${height * 0.3}" r="${Math.min(width, height) * 0.2}" fill="${accent}" opacity="0.4"/>
    <circle cx="${width * 0.3}" cy="${height * 0.7}" r="${Math.min(width, height) * 0.15}" fill="${accent}" opacity="0.3"/>
    <rect x="${width * 0.1}" y="${height * 0.4}" width="${width * 0.3}" height="${height * 0.2}" rx="8" fill="white" opacity="0.15"/>
    ${text ? `<text x="${width/2}" y="${height/2}" font-family="Inter,sans-serif" font-size="${Math.min(width,height)*0.08}" fill="white" text-anchor="middle" dominant-baseline="middle" opacity="0.7">${text}</text>` : ''}
  </svg>`;
  return `data:image/svg+xml,${encodeURIComponent(svg)}`;
}

function generateAvatarUrl(name, colors) {
  const initials = name.split(' ').map(n => n[0]).join('');
  const [bg, fg] = colors;
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="200" height="200">
    <defs>
      <linearGradient id="g" x1="0%" y1="0%" x2="100%" y2="100%">
        <stop offset="0%" style="stop-color:${bg}"/>
        <stop offset="100%" style="stop-color:${fg}"/>
      </linearGradient>
    </defs>
    <rect width="200" height="200" rx="100" fill="url(#g)"/>
    <text x="100" y="108" font-family="Inter,sans-serif" font-weight="600" font-size="72" fill="white" text-anchor="middle" dominant-baseline="middle">${initials}</text>
  </svg>`;
  return `data:image/svg+xml,${encodeURIComponent(svg)}`;
}

